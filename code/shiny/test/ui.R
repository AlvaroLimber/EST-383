library(shiny)

# Define UI for application that draws a histogram
shinyUI(fluidPage(

    # Application title
    titlePanel("Test 1"),

    # Sidebar with a slider input for number of bins
    sidebarLayout(
        sidebarPanel(
            sliderInput("bins",
                        "Boton 1:",
                        min = 1,
                        max = 70,
                        value = 10),
            sliderInput("bins1",
                        "Boton 2:",
                        min = 1,
                        max = 70,
                        value = 10)
        ),

        # Show a plot of the generated distribution
        mainPanel(
            plotOutput("distPlot"),
        )
    )
))
